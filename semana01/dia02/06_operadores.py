"""Operadores aritméticos"""
# Suma
print(3 + 2)

# Resta
print(3 - 2)

# Multiplicación
print(3 * 2)

# División
print(3 / 2)

# División entera
print(3 // 2)

# Módulo
print(3 % 2)

# Potencia
print(3 ** 2)

"""Operadores de comparación"""
# Igualdad
print(3 == 2)

# Diferencia
print(3 != 2)

# Mayor que
print(3 > 2)

# Menor que
print(3 < 2)

# Mayor o igual que
print(3 >= 2)

# Menor o igual que
print(3 <= 2)

"""Operadores lógicos"""
# And
print(True and False)

# Or
print(True or False)

# Not
print(not True)

"""Operadores de asignación"""
# Asignación
a = 3

# Suma en asignación
a += 2 # a = a + 2

# Resta en asignación
a -= 2 # a = a - 2

# Multiplicación en asignación
a *= 2 # a = a * 2

# División en asignación
a /= 2 # a = a / 2

"""Operadores de identidad"""
# is
print(3 is 2)

# is not
print(3 is not 2)

"""Operadores de membresía"""
# in
print(3 in (1, 2, 3))

# not in
print(3 not in (1, 2, 3))
